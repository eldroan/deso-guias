package isi.deso.died.guia2;

/**
 *
 * @author leandroamarillo
 */
public class Punto {

    private float x;
    private float y;

    public Punto() {
        x = 0;
        y = 0;

    }

    public Punto(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Punto other = (Punto) obj;
        if (this.x != other.x) {
            return false;
        }
        return this.y == other.y;
    }

    public void setY(int y) {
        this.y = y;
    }

}
